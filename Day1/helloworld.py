# Calculator Operations
print(5+5) # addition
print(8-3) # subtraction
print(6*3) # multiplication
print(7/3) # division
print(8**5) # exponential
print(10%3) # modular
print(10//2) # floor division operator

# Data Types
print(type(10)) # int
print(type(3.14)) # float
print(type(1+3j)) # complex
print(type("John Wayne")) # string
print(type([1,2,3])) # list
print(type({"Paris", "Tokyo", "Shanghai"})) # dictionary
print(type({7.1,6.9,5.1})) # set
print(type((2.3,4.5,6.1))) # tuple